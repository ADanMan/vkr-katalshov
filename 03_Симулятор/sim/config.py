"""
sim.config — единый источник физических параметров стенда S1
с использованием pint для строгой работы с единицами измерения.

Принципы:
* Все физические величины — pint.Quantity, не голые числа.
* Параметры PT100 — по ГОСТ 6651-2009.
* Параметры АЦП — типовой 16-битный измерительный модуль.
* Параметры процесса — нормативная методика (см. dsl_v1.yaml).

При изменении параметров — синхронизировать с
02_Спецификация/dsl_v1.yaml.
"""

from __future__ import annotations

import pint

# Глобальный реестр единиц. Используется во всём пакете.
ureg = pint.UnitRegistry()
ureg.formatter.default_format = "~P"  # компактный pretty-print: "150 °C"
Quantity = ureg.Quantity


# ═══════════════════════════════════════════════════════════════════
# 1. Физика теплопередачи (термокамера + образец)
# ═══════════════════════════════════════════════════════════════════

# Постоянная времени системы (объединённая ёмкость камера+образец).
# Типичное значение для лабораторной термокамеры с принудительной
# конвекцией и образцом средней массы.
TAU = Quantity(60.0, "s")

# Начальная температура (комнатная) перед стартом прогона.
T_AMBIENT = Quantity(24.0, "degC")

# СКО гауссовского шума температурного сигнала в самой системе
# (термические флуктуации, неравномерность поля камеры). Это ШУМ
# ОБЪЕКТА, а не сенсора — последний моделируется отдельно.
SYSTEM_NOISE_SIGMA = Quantity(0.05, "delta_degC")


# ═══════════════════════════════════════════════════════════════════
# 2. Сенсор PT100 (ГОСТ 6651-2009, IEC 60751)
# ═══════════════════════════════════════════════════════════════════

# Сопротивление при 0 °C
R0 = Quantity(100.0, "ohm")

# Коэффициенты формулы Каллендар–Ван Дюссена для T ≥ 0 по
# ГОСТ 6651-2009 п. 5.1.1 (соответствует МЭК 60751):
#   R(T) = R0 * (1 + ALPHA·T + BETA·T²)
# Эти коэффициенты дают нормативное соотношение
# W₁₀₀ = R(100°C)/R(0°C) = 1 + 100·α + 10000·β = 1.385 ровно,
# что отвечает платине с TCR α₀ = 0.00385 °C⁻¹ — стандарт IEC.
# (Внимание: ALPHA здесь — коэффициент квадратичной модели, не TCR;
#  численно близки, но семантически разные величины.)
ALPHA = Quantity(3.9083e-3, "1 / kelvin")
BETA = Quantity(-5.775e-7, "1 / kelvin**2")

# Класс точности PT100 по ГОСТ 6651-2009 табл. A.1.
# Класс A: ±(0.15 + 0.002·|T|) °C; класс B: ±(0.30 + 0.005·|T|) °C.
# Используется в uncertainty_model.pt100_class_tolerance_C().
SENSOR_CLASS = "A"

# Тепловая инерция самого чувствительного элемента — постоянная
# времени теплового RC-фильтра (отставание T_int от T_object).
# Типично для тонкоплёночного PT100 ≈ 1–3 c.
SENSOR_TIME_CONSTANT = Quantity(2.0, "s")

# Самонагрев PT100 от тока возбуждения (ГОСТ 6651-2009 п. 5.4).
# Для типового мостового включения с I = 1 мА, R ≈ 100…140 Ом,
# мощность P = I²·R ≈ 0.1–0.14 мВт, при типовом коэффициенте
# рассеяния тепла k_рассеяния ≈ 5 мВт/К → δT_self ≈ 0.02–0.03 °C.
# Берём консервативно 0.05 °C как границу систематической составляющей
# (равномерное распределение).
SENSOR_SELF_HEATING_C = Quantity(0.05, "delta_degC")


# ═══════════════════════════════════════════════════════════════════
# 3. Аналого-цифровой преобразователь (АЦП)
# ═══════════════════════════════════════════════════════════════════

ADC_BITS = 16
ADC_RANGE_MIN = Quantity(0.0, "degC")
ADC_RANGE_MAX = Quantity(500.0, "degC")

# СКО шума самого АЦП (в LSB)
ADC_NOISE_SIGMA_LSB = 0.5

# Дрейф нуля АЦП во времени (актуально для прогонов > 1 час).
ADC_DRIFT_PER_HOUR = Quantity(1.0e-3, "delta_degC / hour")


# ═══════════════════════════════════════════════════════════════════
# 4. Контур управления (PID)
# ═══════════════════════════════════════════════════════════════════

# Параметры PID-регулятора (предварительные; могут быть
# перенастроены автоподбором в control_loop.py).
PID_KP = 2.5      # пропорциональный
PID_KI = 0.05     # интегральный
PID_KD = 0.0      # дифференциальный (часто нулевой для тепловых процессов)

# Ограничение управляющего сигнала (нормированный 0..1; реально —
# скважность ШИМ нагревателя)
PID_OUTPUT_MIN = 0.0
PID_OUTPUT_MAX = 1.0


# ═══════════════════════════════════════════════════════════════════
# 5. Параметры процесса измерения (нормативная методика)
# ═══════════════════════════════════════════════════════════════════

T_SET_DEFAULT = Quantity(150.0, "degC")
T_SET_MAX = Quantity(250.0, "degC")          # верхний предел камеры
T_MIN = Quantity(50.0, "degC")               # порог HEAT → HOLD
DELTA_STABLE = Quantity(0.02, "delta_degC / s")  # порог стабилизации
T_HOLD_MIN = Quantity(300, "s")
T_HOLD_MAX = Quantity(600, "s")
N_MIN = 20                                   # отсчёты в MEASURE


# ═══════════════════════════════════════════════════════════════════
# 6. Параметры симуляции
# ═══════════════════════════════════════════════════════════════════

# Шаг адаптивного интегратора — указывается max_step для solve_ivp.
DT_MAX = Quantity(0.1, "s")

# Частота сэмплирования физического сигнала (для логгера SAMPLE-событий)
F_SAMPLE = Quantity(1.0, "Hz")

# Частота отсчётов в режиме MEASURE
F_MEAS = Quantity(1.0, "Hz")

# Лимит длительности прогона — защита от бесконечного цикла.
RUN_TIMEOUT = Quantity(3600, "s")  # 1 час


# ═══════════════════════════════════════════════════════════════════
# 7. ID стенда (для event-log)
# ═══════════════════════════════════════════════════════════════════

STAND_ID = "S1"
SCENARIO_DEFAULT = "s1_correct"
SIMULATOR_VERSION = "0.1.0"
LOG_FORMAT_VERSION = "1.0"


# ═══════════════════════════════════════════════════════════════════
# Утилитарные функции
# ═══════════════════════════════════════════════════════════════════

def to_seconds(q: Quantity) -> float:
    """Привести величину времени к секундам как float."""
    return q.to("s").magnitude


def to_celsius(q: Quantity) -> float:
    """Привести величину температуры к °C как float."""
    return q.to("degC").magnitude


def to_ohm(q: Quantity) -> float:
    """Привести сопротивление к Омам как float."""
    return q.to("ohm").magnitude


